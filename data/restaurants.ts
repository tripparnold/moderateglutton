export type MichelinTier = 'star' | 'bibgourmand' | 'recommended';
export type PriceTier    = '$' | '$$' | '$$$' | '$$$$';

export interface JBDistinction {
  type:        'semifinalist' | 'nominee' | 'finalist' | 'winner';
  category:    string;
  year:        number | number[];
  /** true when the nomination is for the chef/restaurateur personally, not the restaurant */
  chefAward?:  boolean;
}

export interface Restaurant {
  id:           string;
  name:         string;
  address:      string;
  neighborhood: string;
  cuisine:      string;        // primary cuisine shown on card
  cuisineTags:  string[];      // all cuisine tags for filtering
  price:        PriceTier;
  note:         string;        // Tripp's personal note — empty string if not yet written
  lat:          number;
  lng:          number;
  topRank?:     number;        // 1–10 for "Tripp's Current Go-To's"
  website?:     string;        // official restaurant website
  opentable?:   string;        // full OpenTable URL e.g. https://www.opentable.com/r/tiny-champions-houston
  resy?:        string;        // full Resy URL e.g. https://resy.com/cities/hou/nancys-hustle
  wantToTry?:   boolean;       // true = on Tripp's list, not yet visited
  distinctions?: {
    michelin?:        MichelinTier;
    jamesBeard?:      JBDistinction[];
    texasMonthlyBBQ?: boolean;
  };
}

